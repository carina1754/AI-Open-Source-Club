# 민아  70  56  23  100

# 여러개의 원소를 선택하여 새로운 값 할당
# 모든 원소 같은 값 or 각기 다른 값 변경 가능

df.loc['서준', ['음악', '체육']] = 50
print(df)
print('\n')

df.loc['서준', ['음악', '체육']] = 100, 50
print(df)

